        #region Save_Items
        public AfyaPro_Types.clsResult Save_Items(String mGroupCode,  DataTable mDtItems)
        {
            String mFunctionName = "Save_Items";

            AfyaPro_Types.clsResult mResult = new AfyaPro_Types.clsResult();
            OdbcConnection mConn = new OdbcConnection();
            OdbcCommand mCommand = new OdbcCommand();
            OdbcDataAdapter mDataAdapter = new OdbcDataAdapter();
            OdbcTransaction mTrans = null;

            #region database connection

            try
            {
                mConn.ConnectionString = clsGlobal.gAfyaConStr;

                if (mConn.State != ConnectionState.Open)
                {
                    mConn.Open();
                }

                mCommand.Connection = mConn;
            }
            catch (Exception ex)
            {
                mResult.Exe_Result = -1;
                mResult.Exe_Message = clsGlobal.Write_Error(pClassName, mFunctionName, ex.Message);
                return mResult;
            }

            #endregion

            #region do edit
            try
            {
                mTrans = mConn.BeginTransaction();
                mCommand.Transaction = mTrans;

                DataTable mDtDbItems = new DataTable("dbitems");
                mCommand.CommandText = "select * from facilitycorporateitems where groupcode='" + mGroupCode.Trim() + "'";
                mDataAdapter.SelectCommand = mCommand;
                mDataAdapter.Fill(mDtDbItems);

                DataView mDvDbItems = new DataView();
                mDvDbItems.Table = mDtDbItems;
                mDvDbItems.Sort = "itemcode";

                foreach (DataRow mDataRow in mDtItems.Rows)
                {
                    bool mApplicable = Convert.ToBoolean(mDataRow["selected"]);
                    string mItemCode = mDataRow["code"].ToString().Trim();
                    double mPricingPercent = Convert.ToDouble(mDataRow["percent"]);

                    if (mApplicable == false)
                    {
                        mCommand.CommandText = "delete from facilitycorporateitems where groupcode='"
                        + mGroupCode.Trim() + "' and itemcode='" + mItemCode + "'";
                        mCommand.ExecuteNonQuery();
                    }
                    else
                    {
                        if (mDvDbItems.Find(mItemCode) >= 0)
                        {
                            mCommand.CommandText = "update facilitycorporateitems set pricingpercent=" + mPricingPercent
                            + " where groupcode='" + mGroupCode.Trim() + "' and itemcode='" + mItemCode + "'";
                            mCommand.ExecuteNonQuery();
                        }
                        else
                        {
                            mCommand.CommandText = "insert into facilitycorporateitems(groupcode,itemcode,"
                            + "pricingpercent) values('" + mGroupCode.Trim() + "','" + mItemCode.Trim() + "',"
                            + mPricingPercent + ")";
                            mCommand.ExecuteNonQuery();
                        }
                    }
                }

                mTrans.Commit();

                //return
                return mResult;
            }
            catch (OdbcException ex)
            {
                //rollback
                try { mTrans.Rollback(); }
                catch { }
                mResult.Exe_Result = -1;
                mResult.Exe_Message = clsGlobal.Write_Error(pClassName, mFunctionName, ex.Message);
                return mResult;
            }
            finally
            {
                mConn.Close();
            }
            #endregion
        }
